    private void check() {
        if (new File(jTextFieldPfadProjekt.getText()).isFile()) {
            daten.system[Konstanten.SYSTEM_PROJECTDATEI_PFAD_NR] = jTextFieldPfadProjekt.getText();
            ok = true;
        }
    }

    private void beenden() {
        this.dispose();

    }

    class OkBeobachter implements ActionListener {

        public void actionPerformed(ActionEvent e) {
            check();
            beenden();
        }

    }

    class AbbrechenBeobachter implements ActionListener {

        public void actionPerformed(ActionEvent e) {
            ok = false;
            beenden();
        }

    }

